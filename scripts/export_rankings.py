#!/usr/bin/env python3
"""Export results/*.jsonl -> rankings.json (the contract consumed by the leaderboard + router).

Reuses mixture_analysis (which reuses run_cybersoceval) so scoring is identical to the harness.
Emits both rankings.json (for programmatic consumers) and data/rankings.js (a `window.RANKINGS`
shim the static leaderboard can <script src>, avoiding fetch/CORS from file://).

Cost columns need provider prices — put a .env with TOGETHER_API_KEY next to the harness, or
costs fall back to 0. Scores/reliability are always exact.
"""
import json
import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent
ROOT = HERE.parent
sys.path.insert(0, str(ROOT))
import mixture_analysis as mx   # noqa: E402  (imports run_cybersoceval as mx.h)

DISPLAY = {"opus-4.8": "Claude Opus 4.8", "gpt-5.5": "GPT-5.5", "gpt-5.1": "GPT-5.1",
           "minimax-m3": "MiniMax M3", "qwen3-235b": "Qwen3-235B", "glm-5.2": "GLM-5.2",
           "gpt-oss-120b": "gpt-oss-120b"}
CLOSED = {"opus-4.8", "gpt-5.5", "gpt-5.1"}
POOL3 = ["minimax-m3", "qwen3-235b", "glm-5.2"]

DOMAINS = [
    ("cti", "Threat-Intel Reasoning",
     "CyberSOCEval (Meta × CrowdStrike) · CTI task · metric: Jaccard", mx.load_cti),
    ("malware", "Malware Analysis",
     "CyberSOCEval (Meta × CrowdStrike) · malware task · metric: Jaccard", mx.load_malware),
]


def build(loader):
    recs = loader()
    byq, correct = mx.index(recs)
    present = [m for m in mx.ALL_MODELS if any(m in byq[q] for q in correct)]
    weights = {m: mx.single_stats(byq, correct, m)["meanJac"] for m in present}
    rows = []
    for m in present:
        s = mx.single_stats(byq, correct, m)
        rows.append({"model": DISPLAY.get(m, m), "type": "closed" if m in CLOSED else "open",
                     "score": round(s["meanJac"], 3), "exact_pct": round(s["exact"], 1),
                     "answered_pct": round(s["answered"]), "cost_per_1k_usd": round(s["cost_per_1k"], 2)})
    sm = mx.mixture_stats(byq, correct, POOL3, "majority", weights)
    rows.append({"model": "Mixture — 3 open models", "type": "mixture",
                 "score": round(sm["meanJac"], 3), "exact_pct": round(sm["exact"], 1),
                 "answered_pct": round(sm["answered"]), "cost_per_1k_usd": round(sm["cost_per_1k"], 2)})
    rows.sort(key=lambda r: -r["score"])
    return rows, len(correct)


def main():
    mx.h.load_env(ROOT / ".env")
    mx.h.load_prices()
    domains, n_used = {}, 30
    for did, name, bench, loader in DOMAINS:
        try:
            rows, n = build(loader)
        except Exception as e:
            print(f"skip {did}: {type(e).__name__}: {e}")
            continue
        n_used = n
        domains[did] = {"name": name, "benchmark": bench, "metric": "jaccard", "models": rows}
    if not domains:
        print("no domains exported — check results/ and dataset paths"); return
    out = {"meta": {"suite": "CyberSOCEval (Meta × CrowdStrike)", "n": n_used,
                    "generated": "unset (stamp on publish)"}, "domains": domains}

    (ROOT / "rankings.json").write_text(json.dumps(out, indent=2))
    js = "// generated by scripts/export_rankings.py — do not edit by hand\n" \
         "window.RANKINGS = " + json.dumps(out, indent=2) + ";\n"
    (ROOT / "rankings.js").write_text(js)
    print(f"wrote rankings.json + rankings.js ({len(domains)} domains, n={n_used})")
    print("copy rankings.js -> ../security-llm-leaderboard/data/rankings.js to update the site")


if __name__ == "__main__":
    main()
